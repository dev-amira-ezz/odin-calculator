"use strict";

// ======= Math Functions =======
// Add
const add = (num1, num2) => {
    return parseFloat(num1) + parseFloat(num2);
}

// Subtract
const subtract = (num1, num2) => {
    return num1 - num2;
}

// Multiply
const multiply = (num1, num2) => {
    return num1 * num2;
}

// Divide
const divide = (num1, num2) => {
    return num1 / num2;
}

